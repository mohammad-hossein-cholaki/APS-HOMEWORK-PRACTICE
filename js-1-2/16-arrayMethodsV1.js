let array = ["salem",15,18,"str"]
console.log(array);

// Push – add to 
array.push(null,undefined)
console.log(array);

// Pop – remove from end
array.pop()
array.pop()
console.log(array);

// Unshift – add to start
array.unshift(null,undefined,"dssdf",15545)
console.log(array);

// Shift – remove from start
array.shift()
array.shift()
console.log(array);